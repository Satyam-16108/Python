# Arithmetic Operator
a = 2
b = 4
# Addition of numbers
add = a + b
print(add)
# Subtraction of numbers
sub = a - b
print(sub)
# Multiplication of number
mul = a * b
print(mul)
# Division(float) of number
div1 = a / b
print(div1)
# Division(floor) of number
div2 = a // b
print(div2)
# Modulo of both number
mod = a % b
print(mod)
# Power
p = a ** b
print(p)


# Relational Operators

# a > b is False
print(a > b)

# a < b is True
print(a < b)

# a == b is False
print(a == b)

# a != b is True
print(a != b)

# a >= b is False
print(a >= b)

# a <= b is True
print(a <= b)

# Logical Operator
a = True
b = False

# Print a and b is False
print(a and b)

# Print a or b is True
print(a or b)

# Print not a is False
print(not a)


# Bitwise operators
a = 10
b = 4

# Print bitwise AND operation
print(a & b)

# Print bitwise OR operation
print(a | b)

# Print bitwise NOT operation
print(~a)

# print bitwise XOR operation
print(a ^ b)

# print bitwise right shift operation
print(a >> 2)
""" a<<n --> a*2^n
    a>>n --> a/2^n """

# print bitwise left shift operation
print(a << 2)
""" a<<n --> a*2^n
    a>>n --> a/2^n """
